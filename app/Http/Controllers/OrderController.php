    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'no_hp' => 'required',
            'status' => 'required',
            'jumlah' => 'required',
            'sumofprice' => 'required',
        ]);

        Promo::create([
            'name' => $request->name,
            'no_hp' => $request->no_hp,
            'category_id' => Category::id(),
            'paket_id' => Paket::id(),
            'status' => $request->status,
            'body' => $request->body,
        ]);
        return redirect()->route('promo.index');
    }

